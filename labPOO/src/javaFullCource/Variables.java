package javaFullCource;

public class Variables {

	public static void main(String[] args) {
		int x = 123;
		long y = 3217317298379217L;
		float z = 3.14f;
		double w = 3.141592653589793238462643383279502884;
		boolean h = true;
		char symbol = '@';
		String name = "Cloves Paiva";
		
		System.out.println("Este é um inteiro curto: " + x);
		System.out.println("Este é um inteiro longo: " + y);
		System.out.println("Este é uma decimal curto: " + z);
		System.out.println("Este é um decimal longo: " + w);
		System.out.println("Este é um booleano: " + h);
		System.out.println("Este é um caracter: " + symbol);
		System.out.println("Este é uma string: " + name);
	}

}
